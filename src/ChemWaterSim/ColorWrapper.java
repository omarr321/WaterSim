package ChemWaterSim;

public class ColorWrapper {
    int red, blue, green;
    public ColorWrapper(int red, int green, int blue){
        this.red = red;
        this.blue = blue;
        this.green = green;
    }

    public int getRed() {
        return  this.red;
    }

    public int getBlue() {
        return this.blue;
    }
    public int getGreen() {
        return this.green;
    }
}
